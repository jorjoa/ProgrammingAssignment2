makeCacheMatrix <- function(x = matrix()) {
  m <- NULL
  set <- function(y) {
    x <<- y
    m <<- NULL
  }
  get <- function() x
  setinverse <- function(inverse) m <<- inverse
  getinverse <- function() m
  list(set = set, get = get,
       setinverse = setinverse,
       getinverse = getinverse)
}

## Given a matrix:
# get: returns the matrix stored
# set: changes the stored matrix
# setinverse: stores the inverse matrix
# getinverse: returns the inverse matrix stored
#All of those functions are stored in a list for the previous use in the cacheSolve function

